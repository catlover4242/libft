#include "libft.h"
#include <stdlib.h>

char	*ft_strdup(const char *s)
{
	char	*string;
	size_t	i;

	i = 0;
	string = malloc(sizeof(char) * (ft_strlen(s) + 1));
	if (string == NULL)
		return (NULL);
	while (s[i] != '\0')
	{
		string[i] = s[i];
		i++;
	}
	string[i] = '\0';
	return (string);
}
